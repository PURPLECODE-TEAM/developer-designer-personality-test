export default function convertNameToResult(name) {
  if (name === 'EFJ') {
    return 'fox';
  }
  if (name === 'EFP') {
    return 'capybara';
  }
  if (name === 'ETJ') {
    return 'pelican';
  }
  if (name === 'ETP') {
    return 'monkey';
  }
  if (name === 'IFJ') {
    return 'squirrel';
  }
  if (name === 'IFP') {
    return 'sloth';
  }
  if (name === 'ITJ') {
    return 'bear';
  }
  if (name === 'ITP') {
    return 'cat';
  }
}
