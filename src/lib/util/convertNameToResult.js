export default function convertNameToResult(name) {
  if (name === 'fox') {
    return 'EFJ';
  }
  if (name === 'capybara') {
    return 'EFP';
  }
  if (name === 'pelican') {
    return 'ETJ';
  }
  if (name === 'monkey') {
    return 'ETP';
  }
  if (name === 'squirrel') {
    return 'IFJ';
  }
  if (name === 'sloth') {
    return 'IFP';
  }
  if (name === 'bear') {
    return 'ITJ';
  }
  if (name === 'cat') {
    return 'ITP';
  }
}
